export function getRandomSong(excludeIndices = []) {
  const available = SONG_DATABASE
    .map((song, i) => ({ song, index: i }))
    .filter(({ index }) => !excludeIndices.includes(index));

  if (available.length === 0) {
    return { song: SONG_DATABASE[0], index: 0 };
  }
  
  const pick = available[Math.floor(Math.random() * available.length)];
  return { song: pick.song, index: pick.index };
}

export function getSongCount() {
  return SONG_DATABASE.length;
}

